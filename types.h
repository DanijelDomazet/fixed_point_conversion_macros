#ifndef SRC_TYPES_H_
#define SRC_TYPES_H_


#include <stdint.h>

/**
* @brief Fixed point signed 8-bit number.
*/
typedef int8_t fix8_t;

/**
* @brief Fixed point signed 16-bit number.
*/
typedef int16_t fix16_t;

/**
* @brief Fixed point signed 32-bit number.
*/
typedef int32_t fix32_t;

/**
* @brief Fixed point signed 64-bit number.
*/
typedef int64_t fix64_t;

/**
* @brief 32-bit floating-point type definition.
*/
typedef float float32_t;

/**
* @brief 64-bit floating-point type definition.
*/
typedef double float64_t;


#endif /* SRC_TYPES_H_ */
